# Programa: Ejemplo de caracteres especiales en Python

# Salto de línea \n
mensaje1 = "Hola\nMundo"
print(mensaje1)

# Tabulación \t
mensaje2 = "\tJonathan"
print(mensaje2)

# Comillas dentro de cadena
mensaje3 = "Ella dijo: \"Hola a todos\""
print(mensaje3)

# Barra invertida \
mensaje4 = "C:\\Usuarios\\Jonathan\\Documentos"
print(mensaje4)

# Cadena cruda (row string) r
mensaje5 = r"C:\Usuarios\Jonathan\Documentos"
print(mensaje5)

